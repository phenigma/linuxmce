#include "SafetyLock.h"

SafetyLock::SafetyLock(pthread_mutex_t* Mutex)
{
	this->Mutex = Mutex;
	pthread_mutex_lock(Mutex);
}


SafetyLock::~SafetyLock()
{
	pthread_mutex_unlock(Mutex);
}


