#elif defined(__MARM__) && !defined(__arm__)
#define __arm__
#elif defined(__MCORE__) && !defined(__mcore__)
#define __mcore__
#endif



//////////////////////////////////////////////////////////////////////////////
//
// CPU Detection
//
//////////////////////////////////////////////////////////////////////////////

#if defined(__arm__)
#define PHAL_CPU_ARM

#elif defined(__i386__)
#define PHAL_CPU_X86

#else
#error "Unknown CPU"

#endif



